def generate_timing(output_path: str) -> None:
    """Generate timing.ts with all frame calculation utilities."""
    content = '''\
// Auto-generated by migrate_assets.py — do not edit manually
import { VO_DURATIONS } from './resourceMap';
import { FPS } from './constants';

/**
 * Convert seconds to frames (always integer)
 */
export function secToFrames(sec: number): number {
  return Math.round(sec * FPS);
}

/**
 * Convert milliseconds to frames (always integer)
 */
export function msToFrames(ms: number): number {
  return Math.round((ms / 1000) * FPS);
}

/**
 * Ensure minimum 1-frame duration (prevents interpolate errors)
 */
export function safeDuration(frames: number): number {
  return Math.max(frames, 1);
}

/**
 * Calculate shot duration from voiceover references
 */
export function calculateShotDuration(
  voIds: (keyof typeof VO_DURATIONS)[],
  bufferMs = 700
): number {
  const totalVoMs = voIds.reduce((sum, id) => sum + (VO_DURATIONS[id] || 0), 0);
  return msToFrames(totalVoMs + bufferMs);
}

/**
 * Calculate cumulative start frame for a shot
 */
export function calculateShotStart(
  shotIndex: number,
  shotDurations: number[]
): number {
  return shotDurations.slice(0, shotIndex).reduce((sum, d) => sum + d, 0);
}

/**
 * Get animation speed multiplier from storyboard pacing
 */
export function pacingMultiplier(pacing: 'slow' | 'medium' | 'fast' | 'pause'): number {
  const map = { slow: 0.6, medium: 1.0, fast: 1.5, pause: 0.3 };
  return map[pacing] ?? 1.0;
}

/**
 * Convert visual_tension (0-1) to spring animation config
 */
export function tensionToSpringConfig(tension: number): { damping: number; stiffness: number } {
  const damping = Math.round(25 - tension * 20);  // 25→5
  const stiffness = Math.round(80 + tension * 270); // 80→350
  return { damping: Math.max(5, damping), stiffness };
}

/**
 * Calculate audio offset in frames from audio_visual_relation
 */
export function audioOffsetFrames(
  relation: 'sync' | 'lead-visual' | 'lead-audio' | 'counterpoint',
  fps: number,
  offsetMs?: number,
): number {
  if (relation === 'sync' || relation === 'counterpoint') return 0;
  const defaultMs = relation === 'lead-visual' ? 500 : -300;
  return Math.round((offsetMs ?? defaultMs) / 1000 * fps);
}
'''
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(content)



def main():
    parser = argparse.ArgumentParser(description="Migrate assets and generate TypeScript files")
    parser.add_argument("--project-path", required=True, help="Project root directory")
    parser.add_argument("--raws", default="raws", help="Raw assets dir (relative to project-path)")
    parser.add_argument("--public", default="template-project/public", help="Remotion public dir")
    parser.add_argument("--resources", default="manifests/resources.yaml", help="resources.yaml path")
    parser.add_argument("--durations", default="raws/audio/vo/durations.json", help="durations.json path")
    parser.add_argument("--output-dir", default="template-project/src/lib", help="TypeScript output dir")
    parser.add_argument("--skip-copy", action="store_true", help="Skip asset copy, only generate TS")

    args = parser.parse_args()
    project = Path(args.project_path)

    try:
        # Step 1: Copy assets
        if not args.skip_copy:
            raws_dir = project / args.raws
            public_dir = project / args.public
            if not raws_dir.is_dir():
                print(f"Error: raws directory not found: {raws_dir}", file=sys.stderr)
                sys.exit(1)
            copied = copy_assets(str(raws_dir), str(public_dir))
            images = [f for f in copied if f.startswith('images/')]
            audio = [f for f in copied if f.startswith('audio/')]
            videos = [f for f in copied if f.startswith('video')]
            print(f"Copied: {len(images)} images, {len(audio)} audio, {len(videos)} videos")

        # Step 2: Load resources.yaml
        resources = None
        res_path = Path(args.resources) if Path(args.resources).is_absolute() else project / args.resources
        if res_path.exists():
            resources = load_yaml(str(res_path))

        # Step 3: Load durations.json
        durations = None
        dur_path = Path(args.durations) if Path(args.durations).is_absolute() else project / args.durations
        if dur_path.exists():
            with open(dur_path, 'r', encoding='utf-8') as f:
                durations = json.load(f)
        else:
            print(f"Warning: {dur_path} not found", file=sys.stderr)

        # Step 4: Generate TypeScript files
        output_dir = project / args.output_dir

        generate_resource_map(resources, durations, str(output_dir / "resourceMap.ts"), raws_dir=str(project / args.raws))
        generate_constants(str(output_dir / "constants.ts"))
        generate_timing(str(output_dir / "timing.ts"))

        # Copy durations.json to src/lib/ for direct import
        if durations and dur_path.exists():
            dst_dur = output_dir / "durations.json"
            shutil.copy2(str(dur_path), str(dst_dur))

        # Summary
        parts = ["resourceMap.ts", "constants.ts", "timing.ts"]
        if durations:
            total_ms = sum(durations.values())
            parts.append(f"durations.json ({len(durations)} VOs, {total_ms/1000:.0f}s)")
        print("Output dir: ", output_dir)
        print(f"Generated: {', '.join(parts)}")
        print("REMIND: EDIT constants.ts (COLORS & FONTS from memo.md), then `npx tsc --noEmit`")

    except Exception as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
