#!/usr/bin/env python3
"""
Asset Migration Pipeline

Copies raw assets from raws/ to Remotion's public directory and generates
TypeScript files (resourceMap.ts, constants.ts, timing.ts).

Agent 只需在生成后 edit constants.ts 中的 COLORS 和 FONTS（从 memo.md 提取）。

Usage:
    python migrate_assets.py --project-path /path/to/project
    python migrate_assets.py --project-path /path/to/project --skip-copy
"""

import argparse
import json
import os
import shutil
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional

try:
    import yaml
except ImportError:
    yaml = None


def load_yaml(path: str) -> Dict[str, Any]:
    """Load YAML file, with fallback if PyYAML not installed."""
    if yaml is None:
        print("Warning: PyYAML not installed, using JSON fallback", file=sys.stderr)
        json_path = path.replace('.yaml', '.json').replace('.yml', '.json')
        if Path(json_path).exists():
            with open(json_path, 'r', encoding='utf-8') as f:
                return json.load(f)
        raise ImportError("Install PyYAML: pip install pyyaml")

    with open(path, 'r', encoding='utf-8') as f:
        return yaml.safe_load(f)


def copy_assets(raws_dir: str, public_dir: str) -> List[str]:
    """Copy all assets from raws to public directory."""
    copied = []
    raws_path = Path(raws_dir)
    public_path = Path(public_dir)
    public_path.mkdir(parents=True, exist_ok=True)

    for root, dirs, files in os.walk(raws_path):
        dirs[:] = [d for d in dirs if not d.startswith('.')]
        for file in files:
            if file.startswith('.') or file.endswith('.DS_Store'):
                continue
            # Skip non-asset files
            if file.endswith(('.json', '.yaml', '.yml', '.md', '.txt', '.pdf')):
                continue

            src = Path(root) / file
            rel_path = src.relative_to(raws_path)
            dst = public_path / rel_path
            dst.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(str(src), str(dst))
            copied.append(str(rel_path))

    return copied


def generate_resource_map(
    resources: Optional[Dict],
    durations: Optional[Dict],
    output_path: str,
    raws_dir: Optional[str] = None,
) -> None:
    """Generate resourceMap.ts from resources.yaml and actual files."""
    imports = [
        "import { staticFile } from 'remotion';",
    ]
    # durations.json import added conditionally below
    if durations:
        imports.append("import _durations from './durations.json';")

    lines = [
        '// Auto-generated by migrate_assets.py — do not edit manually',
        *imports,
        '',
    ]

    # Visual resources from resources.yaml
    visuals = []
    audios = []
    if resources:
        for item in resources.get('resources', {}).get('visual', []):
            visuals.append(item)
        for item in resources.get('resources', {}).get('audio', []):
            audios.append(item)

    # Images — scan actual files
    if raws_dir:
        images_dir = Path(raws_dir) / 'images'
        if images_dir.is_dir():
            image_files = sorted(
                f for f in images_dir.rglob('*')
                if f.is_file() and not f.name.startswith('.')
                and f.suffix.lower() not in ('.json', '.yaml', '.yml', '.md', '.txt', '.pdf')
            )
            if image_files:
                lines.append('// Image resources')
                lines.append('export const IMAGES = {')
                seen_keys = set()
                for f in image_files:
                    rel = f.relative_to(Path(raws_dir))
                    key = f.stem.replace('-', '_').replace(' ', '_').replace('.', '_')
                    if key in seen_keys:
                        print(f"Warning: duplicate image key '{key}' from {f.name}, skipping", file=sys.stderr)
                        continue
                    seen_keys.add(key)
                    lines.append(f"  {key}: staticFile('{rel}'),")
                lines.append('} as const;')
                lines.append('')

    # Voiceover resources
    if durations:
        lines.append('// Voiceover resources')
        lines.append('export const VOICEOVER = {')
        for vo_id in sorted(durations.keys()):
            path = f'audio/vo/{vo_id}.wav'
            lines.append(f"  {vo_id}: staticFile('{path}'),")
        lines.append('} as const;')
        lines.append('')

        lines.append('// Voiceover durations — imported from durations.json (single source of truth)')
        lines.append('export const VO_DURATIONS = _durations as Record<keyof typeof VOICEOVER, number>;')
        lines.append('')

    # BGM resources
    bgm_files = []
    if raws_dir:
        bgm_dir = Path(raws_dir) / 'audio' / 'bgm'
        if bgm_dir.is_dir():
            bgm_files = sorted(
                f for f in bgm_dir.iterdir()
                if f.is_file() and not f.name.startswith('.')
            )
    if bgm_files:
        lines.append('// BGM resources')
        lines.append('export const BGM = {')
        for f in bgm_files:
            key = f.stem.replace('-', '_').replace(' ', '_')
            lines.append(f"  {key}: staticFile('audio/bgm/{f.name}'),")
        lines.append('} as const;')
        lines.append('')

    # SFX resources
    sfx_files = []
    if raws_dir:
        sfx_dir = Path(raws_dir) / 'audio' / 'sfx'
        if sfx_dir.is_dir():
            sfx_files = sorted(
                f for f in sfx_dir.iterdir()
                if f.is_file() and not f.name.startswith('.')
            )
    if sfx_files:
        lines.append('// SFX resources')
        lines.append('export const SFX = {')
        for f in sfx_files:
            key = f.stem.replace('-', '_').replace(' ', '_')
            lines.append(f"  {key}: staticFile('audio/sfx/{f.name}'),")
        lines.append('} as const;')
        lines.append('')

    # Video resources — scan actual files
    video_files = []
    if raws_dir:
        for vdir_name in ('videos', 'video'):
            vdir = Path(raws_dir) / vdir_name
            if vdir.is_dir():
                video_files = sorted(
                    f for f in vdir.rglob('*')
                    if f.is_file() and not f.name.startswith('.')
                    and f.suffix.lower() not in ('.json', '.yaml', '.yml', '.md', '.txt', '.pdf')
                )
                break
    if video_files and raws_dir:
        lines.append('// Video resources')
        lines.append('export const VIDEO = {')
        seen_keys = set()
        for f in video_files:
            rel = f.relative_to(Path(raws_dir))
            key = f.stem.replace('-', '_').replace(' ', '_').replace('.', '_')
            if key in seen_keys:
                print(f"Warning: duplicate video key '{key}' from {f.name}, skipping", file=sys.stderr)
                continue
            seen_keys.add(key)
            lines.append(f"  {key}: staticFile('{rel}'),")
        lines.append('} as const;')
        lines.append('')

    # Type exports
    lines.append('// Type exports')
    lines.append('export type ImageKey = keyof typeof IMAGES;')
    if durations:
        lines.append('export type VoiceoverKey = keyof typeof VOICEOVER;')
    if bgm_files:
        lines.append('export type BgmKey = keyof typeof BGM;')
    if sfx_files:
        lines.append('export type SfxKey = keyof typeof SFX;')
    if video_files:
        lines.append('export type VideoKey = keyof typeof VIDEO;')
    lines.append('')

    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))


